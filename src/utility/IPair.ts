/**
 * Pair of two elements.
 * 
 * @author Jeongho Nam <http://samchon.org>
 */
export interface IPair<First, Second>
{
    /**
     * The first element.
     */
    first: First;

    /**
     * The second element.
     */
    second: Second;
}